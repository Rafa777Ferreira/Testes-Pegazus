# Criar uma lista vazia
lista = []

# Adicionar itens à lista (pilha) usando append
lista.append("item 1")
lista.append("item 2")
lista.append("item 3")

# Exibir a pilha
print("Pilha inicial:", lista)

# Remover o último item da pilha usando pop
item_removido = lista.pop()

# Exibir o item removido e a pilha após a remoção
print("Item removido:", item_removido)
print("Pilha após remoção:", lista)
